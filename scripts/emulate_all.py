#!/usr/bin/env python3
from __future__ import annotations

import argparse
import sqlite3
from concurrent.futures import ThreadPoolExecutor, as_completed
from datetime import datetime
from pathlib import Path
from typing import Any

try:
    import benchmark_sources
    import emulate
    import vplan_explain
except ModuleNotFoundError:
    from scripts import benchmark_sources, emulate, vplan_explain


DEFAULT_DB_DIR = "artifacts"
DEFAULT_CONCURRENCY = 5
DEFAULT_VFS_DB = "artifacts/vfs.db"

TABLE_COLUMNS = [
    "run_id",
    "created_at",
    "stage",
    "failure",
    "failure_message",
    "bench",
    "use_vf",
    "benchmark",
    "image",
    "simulator_target",
    "len_1d",
    "lmul",
    "timeout_s",
    "effective_timeout_s",
    "docker_exit_code",
    "status",
    "exit_code",
    "wall_time_s",
    "kernel_cycles",
    "total_cycles",
    "sim_speed_khz",
    "artifact_dir",
    "container_log",
    "run_log",
    "trace_file",
    "report_file",
    "docker_command",
    "source",
    "vplan_log_path",
    "vplan_log_text",
    "container_log_text",
    "run_log_text",
    "raw_ll_text",
    "prevec_ll_text",
    "opt_ll_text",
    "asm_text",
]


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Run emulate across all TSVC loop benchmarks using precomputed VF candidates."
    )
    parser.add_argument("--image", default=emulate.DEFAULT_IMAGE, help="Docker image tag")
    parser.add_argument("--len", dest="len_1d", type=int, default=4096, help="LEN_1D value")
    parser.add_argument("--lmul", type=int, default=1, help="LMUL value")
    parser.add_argument("--timeout", type=int, default=120, help="Simulation timeout in seconds")
    parser.add_argument(
        "--log-root",
        default=emulate.DEFAULT_LOG_ROOT,
        help="Host output root for emulate artifacts",
    )
    parser.add_argument(
        "--vplan-log-root",
        default=vplan_explain.DEFAULT_OUTPUT_ROOT,
        help="Host output root for vplan-explain artifacts",
    )
    parser.add_argument("--arch", default="RVV", choices=["RVV", "MAC"], help="Target architecture")
    parser.add_argument("--vlen", type=int, default=128, help="RVV vector length in bits")
    parser.add_argument("--llvm-custom", default="", help="Optional host LLVM build/bin directory")
    parser.add_argument("--concurrency", type=int, default=DEFAULT_CONCURRENCY, help="Parallel emulate job count")
    parser.add_argument(
        "--db-dir",
        default=DEFAULT_DB_DIR,
        help="Directory for emulate-result-YYYYMMDDHHMM.sqlite",
    )
    parser.add_argument(
        "--vfs-db",
        default=DEFAULT_VFS_DB,
        help="Path to artifacts/vfs.db generated by vplan-explain-all",
    )
    return parser.parse_args()


def validate_args(args: argparse.Namespace) -> None:
    emulate.validate_positive_int("len", args.len_1d)
    emulate.validate_positive_int("lmul", args.lmul)
    emulate.validate_positive_int("timeout", args.timeout)
    emulate.validate_positive_int("concurrency", args.concurrency)
    if args.arch == "RVV" and args.vlen <= 0:
        emulate.fail("vlen must be a positive integer")


def discover_benches(root: Path) -> list[str]:
    return benchmark_sources.discover_catalog_benches(root)


def resolve_db_path(root: Path, db_dir: str, run_id: str) -> Path:
    db_root = Path(db_dir)
    if not db_root.is_absolute():
        db_root = (root / db_dir).resolve()
    db_root.mkdir(parents=True, exist_ok=True)
    return db_root / f"emulate-result-{run_id}.sqlite"


def resolve_input_path(root: Path, value: str) -> Path:
    path = Path(value)
    if not path.is_absolute():
        path = (root / value).resolve()
    if not path.exists():
        emulate.fail(f"input file not found: {path}")
    return path


def load_vfs_data(db_path: Path) -> tuple[dict[str, list[dict[str, Any]]], dict[str, dict[str, Any]]]:
    conn = sqlite3.connect(db_path)
    conn.row_factory = sqlite3.Row
    rows = conn.execute(
        """
        SELECT
            bench,
            use_vf,
            raw_vf,
            cost,
            compare,
            plan_index,
            selected,
            failure,
            failure_message,
            source,
            vplan_log_path,
            vplan_log_text
        FROM vfs
        ORDER BY bench, use_vf
        """
    ).fetchall()
    conn.close()

    candidates_by_bench: dict[str, list[dict[str, Any]]] = {}
    failures_by_bench: dict[str, dict[str, Any]] = {}
    for row in rows:
        bench = str(row["bench"] or "")
        if not bench:
            continue
        failure = str(row["failure"] or "")
        record = {
            "bench": bench,
            "use_vf": str(row["use_vf"] or ""),
            "raw_vf": str(row["raw_vf"] or ""),
            "cost": str(row["cost"] or ""),
            "compare": str(row["compare"] or ""),
            "plan_index": row["plan_index"],
            "selected": bool(int(row["selected"] or 0)),
            "failure": failure,
            "failure_message": str(row["failure_message"] or ""),
            "source": str(row["source"] or ""),
            "vplan_log_path": str(row["vplan_log_path"] or ""),
            "vplan_log_text": str(row["vplan_log_text"] or ""),
        }
        if failure:
            failures_by_bench[bench] = record
            continue
        candidates_by_bench.setdefault(bench, []).append(record)
    return candidates_by_bench, failures_by_bench


def make_vplan_result(record: dict[str, Any]) -> dict[str, Any]:
    return {
        "source": record.get("source", ""),
        "vplan_log": record.get("vplan_log_path", ""),
        "vplan_log_text": record.get("vplan_log_text", ""),
        "container_log_text": "",
    }


def make_default_vplan_result() -> dict[str, Any]:
    return {
        "source": "",
        "vplan_log": "",
        "vplan_log_text": "",
        "container_log_text": "",
    }


def create_table(conn: sqlite3.Connection) -> None:
    conn.execute(
        """
        CREATE TABLE IF NOT EXISTS emulate_results (
            run_id TEXT NOT NULL,
            created_at TEXT NOT NULL,
            stage TEXT NOT NULL,
            failure TEXT NOT NULL,
            failure_message TEXT NOT NULL,
            bench TEXT NOT NULL,
            use_vf TEXT NOT NULL,
            benchmark TEXT,
            image TEXT,
            simulator_target TEXT,
            len_1d INTEGER,
            lmul INTEGER,
            timeout_s INTEGER,
            effective_timeout_s INTEGER,
            docker_exit_code INTEGER,
            status TEXT,
            exit_code INTEGER,
            wall_time_s REAL,
            kernel_cycles INTEGER,
            total_cycles INTEGER,
            sim_speed_khz REAL,
            artifact_dir TEXT,
            container_log TEXT,
            run_log TEXT,
            trace_file TEXT,
            report_file TEXT,
            docker_command TEXT,
            source TEXT,
            vplan_log_path TEXT,
            vplan_log_text TEXT,
            container_log_text TEXT,
            run_log_text TEXT,
            raw_ll_text VARCHAR,
            prevec_ll_text VARCHAR,
            opt_ll_text VARCHAR,
            asm_text VARCHAR,
            PRIMARY KEY (bench, use_vf)
        )
        """
    )
    conn.commit()


def make_empty_row(run_id: str, bench: str, use_vf: str) -> dict[str, Any]:
    row = {column: None for column in TABLE_COLUMNS}
    row["run_id"] = run_id
    row["created_at"] = datetime.now().isoformat(timespec="seconds")
    row["stage"] = ""
    row["failure"] = ""
    row["failure_message"] = ""
    row["bench"] = bench
    row["use_vf"] = use_vf
    for column in emulate.BUILD_ARTIFACT_SUFFIXES:
        row[column] = ""
    return row


def insert_row(conn: sqlite3.Connection, row: dict[str, Any]) -> None:
    values = [row.get(column) for column in TABLE_COLUMNS]
    placeholders = ", ".join("?" for _ in TABLE_COLUMNS)
    columns = ", ".join(TABLE_COLUMNS)
    conn.execute(
        f"INSERT OR REPLACE INTO emulate_results ({columns}) VALUES ({placeholders})",
        values,
    )
    conn.commit()


def make_vplan_failure_row(
    *,
    run_id: str,
    bench: str,
    args: argparse.Namespace,
    failure: str,
    message: str,
    vplan_result: dict[str, Any],
) -> dict[str, Any]:
    row = make_empty_row(run_id, bench, "")
    row.update(
        {
            "stage": "vplan",
            "failure": failure,
            "failure_message": message,
            "benchmark": bench,
            "image": args.image,
            "len_1d": args.len_1d,
            "lmul": args.lmul,
            "timeout_s": args.timeout,
            "status": "SKIP",
            "source": vplan_result.get("source"),
            "vplan_log_path": vplan_result.get("vplan_log"),
            "vplan_log_text": vplan_result.get("vplan_log_text") or vplan_result.get("container_log_text"),
            "container_log_text": vplan_result.get("container_log_text"),
        }
    )
    return row


def make_emulate_row(
    *,
    run_id: str,
    bench: str,
    use_vf: str,
    args: argparse.Namespace,
    vplan_result: dict[str, Any],
    emulate_result: dict[str, Any] | None,
    failure: str = "",
    failure_message: str = "",
) -> dict[str, Any]:
    row = make_empty_row(run_id, bench, use_vf)
    row.update(
        {
            "stage": "emulate",
            "failure": failure,
            "failure_message": failure_message,
            "benchmark": bench,
            "image": args.image,
            "len_1d": args.len_1d,
            "lmul": args.lmul,
            "timeout_s": args.timeout,
            "vplan_log_path": vplan_result.get("vplan_log"),
            "vplan_log_text": vplan_result.get("vplan_log_text") or vplan_result.get("container_log_text"),
        }
    )
    if emulate_result is None:
        row["status"] = "ERROR"
        return row

    summary = emulate_result["summary"]
    row.update(summary)
    row["bench"] = bench
    row["use_vf"] = use_vf
    row["container_log_text"] = emulate_result.get("container_log_text", "")
    row["run_log_text"] = emulate_result.get("run_log_text", "")
    for column in emulate.BUILD_ARTIFACT_SUFFIXES:
        row[column] = str(emulate_result.get(column, "") or "")
    return row


def run_emulate_job(
    *,
    bench: str,
    use_vf: str,
    args: argparse.Namespace,
) -> dict[str, Any]:
    return emulate.run_emulate(
        bench=bench,
        image=args.image,
        len_1d=args.len_1d,
        lmul=args.lmul,
        use_vf=use_vf,
        timeout_s=args.timeout,
        log_root=args.log_root,
        ensure_image=False,
    )


def find_missing_artifacts(emulate_result: dict[str, Any], use_vf: str) -> list[str]:
    if not use_vf:
        return []
    return [column for column in emulate.BUILD_ARTIFACT_SUFFIXES if not emulate_result.get(column)]


def main() -> None:
    args = parse_args()
    validate_args(args)

    root = emulate.repo_root()
    run_id = datetime.now().strftime("%Y%m%d%H%M")
    db_path = resolve_db_path(root, args.db_dir, run_id)
    benches = discover_benches(root)

    emulate.ensure_image_exists(args.image)

    conn = sqlite3.connect(db_path)
    create_table(conn)

    print(
        f"emulate-all: benches={len(benches)} parallel={args.concurrency} db={db_path.name}"
    )

    vfs_db_path = resolve_input_path(root, args.vfs_db)
    candidates_by_bench, failures_by_bench = load_vfs_data(vfs_db_path)

    scheduled: list[tuple[str, str, dict[str, Any]]] = []
    any_failure = False
    vplan_failures = 0

    for index, bench in enumerate(benches, start=1):
        failure_record = failures_by_bench.get(bench)
        if failure_record is not None:
            any_failure = True
            vplan_failures += 1
            insert_row(
                conn,
                make_vplan_failure_row(
                    run_id=run_id,
                    bench=bench,
                    args=args,
                    failure=str(failure_record["failure"] or "vplan_failed"),
                    message=str(failure_record["failure_message"] or "vplan-explain failed"),
                    vplan_result=make_vplan_result(failure_record),
                ),
            )
            status_text = "no-vf" if failure_record["failure"] == "no_vf" else "fail"
            print(f"[vplan {index}/{len(benches)}] {bench} {status_text}")
            continue

        candidate_records = candidates_by_bench.get(bench, [])
        if not candidate_records:
            any_failure = True
            vplan_failures += 1
            insert_row(
                conn,
                make_vplan_failure_row(
                    run_id=run_id,
                    bench=bench,
                    args=args,
                    failure="missing_vfs_entry",
                    message="benchmark missing from vfs.db",
                    vplan_result=make_vplan_result({}),
                ),
            )
            print(f"[vplan {index}/{len(benches)}] {bench} missing")
            continue

        print(f"[vplan {index}/{len(benches)}] {bench} vf={len(candidate_records)}")
        for candidate_record in candidate_records:
            scheduled.append((bench, str(candidate_record["use_vf"]), make_vplan_result(candidate_record)))
        scheduled.append((bench, "", make_default_vplan_result()))

    print(f"emulate-jobs: {len(scheduled)}")

    completed = 0
    total_jobs = len(scheduled)
    emulate_failures = 0
    with ThreadPoolExecutor(max_workers=args.concurrency) as executor:
        future_map = {
            executor.submit(
                run_emulate_job,
                bench=bench,
                use_vf=use_vf,
                args=args,
            ): (bench, use_vf, vplan_result)
            for bench, use_vf, vplan_result in scheduled
        }

        for future in as_completed(future_map):
            bench, use_vf, vplan_result = future_map[future]
            completed += 1
            try:
                emulate_result = future.result()
            except Exception as exc:
                any_failure = True
                emulate_failures += 1
                row = make_emulate_row(
                    run_id=run_id,
                    bench=bench,
                    use_vf=use_vf,
                    args=args,
                    vplan_result=vplan_result,
                    emulate_result=None,
                    failure="emulate_exception",
                    failure_message=str(exc),
                )
                insert_row(conn, row)
                print(f"[emu {completed}/{total_jobs}] {bench} {use_vf} fail")
                continue

            failure = ""
            failure_message = ""
            if emulate_result["failed"]:
                any_failure = True
                emulate_failures += 1
                failure = "emulate_failed"
                failure_message = str(emulate_result["summary"].get("status", "emulate failed"))
            else:
                missing_artifacts = find_missing_artifacts(emulate_result, use_vf)
                if missing_artifacts:
                    any_failure = True
                    emulate_failures += 1
                    failure = "artifact_capture_failed"
                    failure_message = f"missing artifacts: {', '.join(missing_artifacts)}"

            row = make_emulate_row(
                run_id=run_id,
                bench=bench,
                use_vf=use_vf,
                args=args,
                vplan_result=vplan_result,
                emulate_result=emulate_result,
                failure=failure,
                failure_message=failure_message,
            )
            insert_row(conn, row)
            status_text = "fail" if failure else str(row.get("status", "done")).lower()
            print(f"[emu {completed}/{total_jobs}] {bench} {use_vf} {status_text}")

    conn.close()
    print(
        f"done: vplan_failures={vplan_failures} emulate_failures={emulate_failures} "
        f"db={db_path}"
    )

    if any_failure:
        raise SystemExit(1)


if __name__ == "__main__":
    main()
